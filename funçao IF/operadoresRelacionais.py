""""
== Igual: 
!+ Diferente de: 
> Maior que: 
>= Maior ou igual: 
< Menor que: 
<= Menor ou igual: 
"""

#Operador ==
letra = "a"
if letra == "a": #if = Se
    print("A letra é igual a letra A")

#Operador !=
if letra != "a": #if = Se
    print("A letra é diferente de A")

#Operador >
numero1 = 20
numero2 = 15
if numero1 > numero2:
    print("O numero 1 é maior que o numero 2")

#Operador <
n1 = 10
n2 = 30
if n1 < n2:
    print("O numero 2 é maior que o numero 1")

#Operador >=
num1 = 50
num2 = 40
if num1 >= 20:
    print("O numero 1 é maior ou igual a 20")

#Operador <= 
if num2 <= 15:
    print("O numero 2 é menor ou igual a 15")