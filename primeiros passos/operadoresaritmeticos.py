"""
+ adicao 
- subtraçao
* multiplicacao
/ divisao
// divisao inteira realiza a divisao entre operandos e a parte decimal de ambos os operantes
% modulo 
** exponenciaçao 
"""

numero1 = 10
numero2 = 5

adicao = (numero1 + numero2)
print(adicao)

subtracao = (numero1 - numero2)
print(subtracao)

multiplicacao = (numero1 * numero2)
print(multiplicacao)

divisao = (numero1 / numero2)
print(divisao)

divisaoInt = (numero1 // numero2)
print(divisaoInt)

modulo = (numero1 % numero2)
print(modulo) 

exponenciacao = (numero1 ** numero2)
print(exponenciacao)