""""
= numero = 1 / Recebe
+= numero = numero + 1 / Incremento
-= numero = numero - 1 / Decremento
*= numero = numero * 1 / Multiplicando
/= numero = numero / 1 / Divide
%= numero = numero % 1 / Resto
"""

#Operador =
numero = 10
print(numero)

#Operador +=
numero += 1
print(numero)

#Operador -=
numero -= 1
print(numero)

#Operador *=
numero *= 2
print(numero)

#Operador /=
numero /= 2
print(numero)

#Operador %=
numero %= 7
print(numero)
